
# Exercise 1: Basic Variable Assignment

# Description: In this exercise, you will practice assigning values to variables of different data types and printing them. This will help you understand how to work with variables in integers, floats, strings, and booleans.

# Task: Assign values to variables of different data types and print them.

# 1. Assign an integer value to a variable and print it.
integer_value = 42
print("Integer value:", integer_value)

# 2. Assign a float value to a variable and print it.
float_value = 3.14
print("Float value:", float_value)

# 3. Assign a string value to a variable and print it.
string_value = "Hello, Python!"
print("String value:", string_value)

# 4. Assign a boolean value to a variable and print it.
boolean_value = True
print("Boolean value:", boolean_value)

# 5. Assign a list of integers to a variable and print it.
list_value = [1, 2, 3, 4, 5]
print("List value:", list_value)

# 6. Assign a dictionary with string keys and integer values to a variable and print it.
dictionary_value = {"one": 1, "two": 2, "three": 3}
print("Dictionary value:", dictionary_value)

# 7. Assign a tuple of strings to a variable and print it.
tuple_value = ("apple", "banana", "cherry")
print("Tuple value:", tuple_value)

# 8. Assign a set of integers to a variable and print it.
set_value = {1, 2, 3, 4, 5}
print("Set value:", set_value)